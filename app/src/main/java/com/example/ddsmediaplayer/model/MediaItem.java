package com.example.ddsmediaplayer.model;

public class MediaItem {

    private String id;
    private String name;
    private String path;
    private int duration;
    private String type;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPath() {
        return path;
    }

    public int getDuration() {
        return duration;
    }

    public String getType() {
        return type;
    }
}
